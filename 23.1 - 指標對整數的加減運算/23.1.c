#include<stdio.h>

int main() {

	int v[5];
	//可以透^㈥列元素的位址加p一整砬蟮闷渌元素位址
	&v[0] + 1 == &v[1]; // v[0] 位址往前移右列元素厚的後的位址
	&v[0] + 1 == &v[2]; // v[1] 位址往前移右列元素厚的後的位址
	&v[0] - 1 == &v[0]; // v[1] 位址往前移右列元素厚的後的位址


	&v[0] + &v[1];			//g失。X）
	&v[2] - &v[1] == 1;		// v[2] 位址到 v[1] 位址距x 1  元素
	&v[3] - &v[0] == 3;		// v[3] 位址到 v[0] 位址距x 3  元素


	return 0;
}