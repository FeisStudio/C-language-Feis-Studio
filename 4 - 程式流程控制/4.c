#pragma warning(disable:4996)
#include<stdio.h>
int main()
{
	int integer1;
	int integer2;
	int sum;
	printf("Please enter the first integer:");
	scanf("%d", &integer1);
	printf("Please enter the second integer:");
	scanf("%d", &integer2);
	sum = integer1 + integer2;
	printf("Sum is %d.\n", sum);
	
	//ol件的绦幸淮(一般述句)
	//有l件的绦幸淮(if述句)
	//ol件的重}绦(oF回圈)
	//有l件的重}绦(while述句)
	//次型重}绦(for述句)
	//x裥缘绦幸淮(switch述句)
	//至少绦幸淮蜗掠l件的重}绦(do-while述句)
	//使用l率:if>for>while>switch>do-while

	return 0;
}