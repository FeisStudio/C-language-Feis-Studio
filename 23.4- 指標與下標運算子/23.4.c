#include<stdio.h>

//指丝梢彩褂孟诉\算子 []

int main() {
	
	int v[5];
	int *n = v;

	n[0] = 0;		//a[b]就是*(a+b)，n[0]就是*(n+0)
	
	v[0] = 0;		//a[b]就是*(a+b)，v[0]就是*(v+0)

	0[v] = 0;		//a[b]就是*(a+b)，0[v]就是*(0+v)

	n[0] == *n;				v[0] == *v;
	n[1] == *(n+1);			v[1] == *(v + 1);
	n[2] == *(n + 2);		v[2] == *(v + 2);

	return 0;
}